package chess;

public class Coordenada {
    private int x, y;
    
    public Coordenada(final int x, final int y) { 
        this.x = x;
        this.y = y;
    }
    
    public final int getX() {
        return x;
    }
    
    public final int getY() {
        return y;
    }
}
